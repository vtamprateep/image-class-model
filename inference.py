def get_class_name(num):
    if num == 0:
        return 'buildings'
    elif num == 1:
        return 'forest'
    elif num == 2:
        return 'glacier'
    elif num == 3:
        return 'mountain'
    elif num == 4:
        return 'sea'
    elif num == 5:
        return 'street'
    else:
        return 'error'